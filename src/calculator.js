
//add function
function add(num1, num2) {
	return num1 + num2;
};

//subtract function
function subtract(num1, num2) {
	return num1 - num2;
};

//multiply function
function multiply(num1, num2) {
	return num1 * num2;
};

//divide function
function divide(num1, num2) {
	return num1 / num2;
};

//square function
function square(num) {
	return num * num;
};

//squareRoot function
function squareRoot(num) {
	return Math.sqrt(num);
};	